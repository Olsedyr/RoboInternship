BX_BOOL formEHCModProc(HBOX hBox, BX_UINT uMsg	,BX_WPARAM wParam, BX_LPARAM lParam)
{
 switch(uMsg)
	{
		case BM_UPDATE:
			{
				formEHCModUpdate(hBox);
				return formEHCModUserUpdate(hBox);
				break;
			}
		case BM_INITDIALOG:
			{
				formEHCModInit(hBox);
				return formEHCModUserInit(hBox,lParam);
				break;
			}
		case BM_COMMAND:
			{
				switch(BX_LOWORD(wParam))
				{
         case FORMEHCMOD_CMDCLOSE:
             return formEHCMod_cmdClose_Click(hBox);
         case FORMEHCMOD_OWORD1:
             return formEHCMod_OWord1_Click(hBox);
         case FORMEHCMOD_OWORD2:
             return formEHCMod_OWord2_Click(hBox);
         case FORMEHCMOD_OWORD3:
             return formEHCMod_OWord3_Click(hBox);
         case FORMEHCMOD_OWORD4:
             return formEHCMod_OWord4_Click(hBox);
         case FORMEHCMOD_OWORD5:
             return formEHCMod_OWord5_Click(hBox);
				}
				break;
			}
		default:
			return(DefBoxProc(hBox,uMsg,wParam,lParam));
	}
	return 0L;
}
